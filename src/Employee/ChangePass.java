    private void confirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmMouseClicked
        String passw = new String(oldPassword.getPassword()).trim();
        String Cpassw = new String(Cpassword.getPassword()).trim();
        String Npass = new String(newPassword.getPassword()).trim();
        dbConnect dbc = new dbConnect();
        Session sess = Session.getInstance();
        dbConnect connector = new dbConnect();
        int userId = 0;
        String uname2 = null;

        if(passw.isEmpty() || Cpassw.isEmpty() || Npass.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please Fill all Boxes");
        }else if(Npass.length() <= 7)
        {
            JOptionPane.showMessageDialog(null, "Password Must be Exactly 8 Characters Long");
        }else if(!Npass.equals(Cpassw))
        {
            JOptionPane.showMessageDialog(null, "Password does not match");
        }else if (Npass.equals(passw))
        {
            JOptionPane.showMessageDialog(null, "New assword Must not be the same as Old Password");
        }else if(Npass.equals(Cpassw))
        {
            try
            {
                String query = "SELECT * FROM tbl_accounts WHERE u_id='"+ sess.getUid() +"'";
                ResultSet rs = dbc .getData(query);
                if(rs.next())
                {
                    String olddbPassword = rs.getString("u_password");
                    String oldhash = passwordHasher.hashPassword(oldPassword.getText());

                    if(olddbPassword.equals(oldhash))
                    {
                        String npass = passwordHasher.hashPassword(newPassword.getText());
                        dbc.updateData("UPDATE tbl_accounts SET u_password = '"+npass+"' WHERE u_id = '"+ sess.getUid() +"'");
                        
                        try 
                        {
                            String query2 = "SELECT * FROM tbl_accounts WHERE u_id = '" + sess.getUid() + "'";
                            PreparedStatement pstmt = connector.getConnection().prepareStatement(query2);

                            ResultSet resultSet = pstmt.executeQuery();

                            if (resultSet.next()) {
                                userId = resultSet.getInt("u_id");   // Update the outer `userId` correctly
                                uname2 = resultSet.getString("u_username");
                            }
                        } catch (SQLException ex) {
                            System.out.println("SQL Exception: " + ex);
                        }

                        logEvent(userId, uname2, "User Changed Their Password");
                        
                        EmployeeDashboard ed = new EmployeeDashboard();
                        ed.setVisible(true);
                        this.dispose();
                    }else
                    {
                        JOptionPane.showMessageDialog(null, "Old Password is Incorrect");
                    }
                }
            } catch(SQLException | NoSuchAlgorithmException ex)
            {
                System.out.println(""+ex);
            }
        }else
        {
            JOptionPane.showMessageDialog(null, "Unknown Error Occured");
        }
    }//GEN-LAST:event_confirmMouseClicked

